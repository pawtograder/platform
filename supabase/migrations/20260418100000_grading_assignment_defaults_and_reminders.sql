  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'assignments_auto_assign_review_due_hours_check'
  ) THEN
    ALTER TABLE public.assignments
      ADD CONSTRAINT assignments_auto_assign_review_due_hours_check
      CHECK (auto_assign_review_due_hours >= 0);
