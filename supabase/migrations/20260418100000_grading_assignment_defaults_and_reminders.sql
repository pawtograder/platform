-- Class-level grading automation profiles + per-assignment defaults/reminders
-- Implements:
-- 1) "Auto assign at deadline" defaults via reusable class profiles
-- 2) Late grading reminders at deadline + recurring interval
-- 3) Instructor-configurable CC + reply-to for reminder emails

CREATE TABLE IF NOT EXISTS public.grading_assignment_default_profiles (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  class_id bigint NOT NULL REFERENCES public.classes(id) ON DELETE CASCADE,
  name text NOT NULL,
  description text,
  auto_assign_at_deadline boolean NOT NULL DEFAULT false,
  auto_assign_assignee_pool text NOT NULL DEFAULT 'graders',
  auto_assign_review_due_hours integer NOT NULL DEFAULT 72,
  late_grading_reminders_enabled boolean NOT NULL DEFAULT false,
  late_grading_reminder_interval_hours integer,
  late_grading_reply_to text,
  late_grading_cc_emails jsonb NOT NULL DEFAULT jsonb_build_object('emails', jsonb_build_array()),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT grading_assignment_default_profiles_assignee_pool_check CHECK (
    auto_assign_assignee_pool IN ('graders', 'instructors', 'instructors_and_graders')
  ),
  CONSTRAINT grading_assignment_default_profiles_due_hours_check CHECK (auto_assign_review_due_hours >= 0),
  CONSTRAINT grading_assignment_default_profiles_reminder_interval_check CHECK (
    (NOT late_grading_reminders_enabled)
    OR (late_grading_reminder_interval_hours IS NOT NULL AND late_grading_reminder_interval_hours > 0)
  ),
  CONSTRAINT grading_assignment_default_profiles_cc_emails_check CHECK (
    jsonb_typeof(late_grading_cc_emails) = 'object'
    AND late_grading_cc_emails ? 'emails'
    AND jsonb_typeof(late_grading_cc_emails->'emails') = 'array'
  ),
  CONSTRAINT grading_assignment_default_profiles_class_name_unique UNIQUE (class_id, name),
  CONSTRAINT grading_assignment_default_profiles_id_class_unique UNIQUE (id, class_id)
);

ALTER TABLE public.grading_assignment_default_profiles ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Class staff can view grading default profiles" ON public.grading_assignment_default_profiles;
CREATE POLICY "Class staff can view grading default profiles"
ON public.grading_assignment_default_profiles
