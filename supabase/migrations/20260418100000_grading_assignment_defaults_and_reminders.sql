  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'assignments_late_grading_reminder_interval_check'
  ) THEN
    ALTER TABLE public.assignments
      ADD CONSTRAINT assignments_late_grading_reminder_interval_check
      CHECK (
        (NOT late_grading_reminders_enabled)
        OR (late_grading_reminder_interval_hours IS NOT NULL AND late_grading_reminder_interval_hours > 0)
      );
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'assignments_late_grading_cc_emails_check'
  ) THEN
    ALTER TABLE public.assignments
      ADD CONSTRAINT assignments_late_grading_cc_emails_check
      CHECK (
        jsonb_typeof(late_grading_cc_emails) = 'object'
        AND late_grading_cc_emails ? 'emails'
        AND jsonb_typeof(late_grading_cc_emails->'emails') = 'array'
      );
  END IF;
END $$;

