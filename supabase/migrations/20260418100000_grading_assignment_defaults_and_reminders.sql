CREATE POLICY "Instructors can insert grading default profiles"
ON public.grading_assignment_default_profiles
AS PERMISSIVE
FOR INSERT
TO authenticated
WITH CHECK (authorizeforclassinstructor(class_id));

DROP POLICY IF EXISTS "Instructors can update grading default profiles" ON public.grading_assignment_default_profiles;
CREATE POLICY "Instructors can update grading default profiles"
ON public.grading_assignment_default_profiles
AS PERMISSIVE
FOR UPDATE
TO authenticated
USING (authorizeforclassinstructor(class_id))
WITH CHECK (authorizeforclassinstructor(class_id));

DROP POLICY IF EXISTS "Instructors can delete grading default profiles" ON public.grading_assignment_default_profiles;
CREATE POLICY "Instructors can delete grading default profiles"
ON public.grading_assignment_default_profiles
AS PERMISSIVE
FOR DELETE
TO authenticated
USING (authorizeforclassinstructor(class_id));

DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM pg_proc p
