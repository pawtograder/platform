-- 9. Admin RPCs for platform registration
-------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.admin_upsert_lti_platform(
    p_id bigint,
    p_name text,
    p_issuer text,
    p_client_id text,
    p_auth_login_url text,
    p_token_url text,
    p_jwks_url text,
    p_enabled boolean DEFAULT true
)
RETURNS public.lti_platforms
LANGUAGE plpgsql SECURITY DEFINER SET search_path TO ''
AS $$
DECLARE
    v_row public.lti_platforms;
BEGIN
    IF NOT public.authorize_for_admin() THEN
        RAISE EXCEPTION 'Access denied: admin role required';
    END IF;

    IF p_id IS NULL THEN
        INSERT INTO public.lti_platforms (name, issuer, client_id, auth_login_url, token_url, jwks_url, enabled)
        VALUES (p_name, p_issuer, p_client_id, p_auth_login_url, p_token_url, p_jwks_url, COALESCE(p_enabled, true))
        RETURNING * INTO v_row;
    ELSE
        UPDATE public.lti_platforms
        SET name = p_name, issuer = p_issuer, client_id = p_client_id,
            auth_login_url = p_auth_login_url, token_url = p_token_url,
            jwks_url = p_jwks_url, enabled = COALESCE(p_enabled, true), updated_at = now()
        WHERE id = p_id
        RETURNING * INTO v_row;
    END IF;

    RETURN v_row;
END;
$$;

REVOKE ALL ON FUNCTION public.admin_upsert_lti_platform(bigint, text, text, text, text, text, text, boolean) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.admin_upsert_lti_platform(bigint, text, text, text, text, text, text, boolean) TO authenticated;
GRANT EXECUTE ON FUNCTION public.admin_upsert_lti_platform(bigint, text, text, text, text, text, text, boolean) TO service_role;

CREATE OR REPLACE FUNCTION public.admin_delete_lti_platform(p_id bigint)
RETURNS void
LANGUAGE plpgsql SECURITY DEFINER SET search_path TO ''
AS $$
BEGIN
    IF NOT public.authorize_for_admin() THEN
        RAISE EXCEPTION 'Access denied: admin role required';
    END IF;
    DELETE FROM public.lti_platforms WHERE id = p_id;
END;
$$;

REVOKE ALL ON FUNCTION public.admin_delete_lti_platform(bigint) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.admin_delete_lti_platform(bigint) TO authenticated;
GRANT EXECUTE ON FUNCTION public.admin_delete_lti_platform(bigint) TO service_role;

