-- LTI 1.3 Integration
--
-- Pawtograder acts as an LTI 1.3 Tool. This migration adds the data model for:
--   * Registered LMS platforms and deployments (lti_platforms, lti_deployments)
--   * Our tool's signing keypairs (lti_tool_keys) -- JWKS published at /api/lti/jwks
--   * Course linkage captured at launch (lti_context_links) with NRPS/AGS endpoints
--   * Assignment <-> AGS line item mapping (lti_line_items)
--   * LTI identity <-> Pawtograder user mapping (lti_users)
--   * Single-use OIDC nonces for replay protection (lti_nonces)
--
-- Authorization model:
--   * Platform/key tables are platform-admin managed (authorize_for_admin()).
--   * Per-course tables are readable by instructors of the class and fully
--     managed by the service role (the Next.js LTI endpoints run with service role).
--   * Roster sync reuses the existing public.sis_sync_enrollment(...) RPC.
--
-- All LTI HTTP/crypto logic lives in Next.js (lib/lti + app/api/lti). See
-- docs/lti-1.3-integration.md.

-------------------------------------------------------------------------------
-- 1. Platforms
-------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS "public"."lti_platforms" (
    "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "created_at" timestamp with time zone DEFAULT now() NOT NULL,
    "updated_at" timestamp with time zone DEFAULT now() NOT NULL,
    "name" text NOT NULL,
    "issuer" text NOT NULL,            -- platform iss claim, e.g. https://canvas.instructure.com
    "client_id" text NOT NULL,         -- our tool's client_id on the platform (= id_token aud)
    "auth_login_url" text NOT NULL,    -- OIDC authentication endpoint (we redirect here on login)
    "token_url" text NOT NULL,         -- OAuth2 token endpoint (client_credentials for NRPS/AGS)
    "jwks_url" text NOT NULL,          -- platform JWKS (we verify id_token signatures against this)
    "enabled" boolean DEFAULT true NOT NULL,
    -- A platform may expose the same issuer for multiple client_ids (rare); the
    -- (issuer, client_id) pair is the unique key per LTI 1.3 core.
    CONSTRAINT "lti_platforms_issuer_client_unique" UNIQUE ("issuer", "client_id")
);

ALTER TABLE "public"."lti_platforms" ENABLE ROW LEVEL SECURITY;
GRANT ALL ON TABLE "public"."lti_platforms" TO "authenticated";
GRANT ALL ON TABLE "public"."lti_platforms" TO "service_role";

CREATE POLICY "Admins manage lti_platforms" ON "public"."lti_platforms"
    FOR ALL USING (authorize_for_admin()) WITH CHECK (authorize_for_admin());
CREATE POLICY "Service role manages lti_platforms" ON "public"."lti_platforms"
    FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

