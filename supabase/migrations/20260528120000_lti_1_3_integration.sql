-- Consume a nonce atomically: returns true if it was unused (and stores it),
-- false if it was already present (replay). TTL-prunes expired nonces.
CREATE OR REPLACE FUNCTION public.lti_consume_nonce(p_nonce text, p_ttl_seconds integer DEFAULT 600)
RETURNS boolean
LANGUAGE plpgsql SECURITY DEFINER SET search_path TO ''
AS $$
DECLARE
    v_rows bigint := 0;
BEGIN
    DELETE FROM public.lti_nonces WHERE expires_at < now();
    INSERT INTO public.lti_nonces (nonce, expires_at)
    VALUES (p_nonce, now() + make_interval(secs => p_ttl_seconds))
    ON CONFLICT (nonce) DO NOTHING;
    -- ROW_COUNT is bigint; capture it explicitly and derive the boolean.
    GET DIAGNOSTICS v_rows = ROW_COUNT;
    RETURN v_rows > 0;
END;
$$;

REVOKE ALL ON FUNCTION public.lti_consume_nonce(text, integer) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.lti_consume_nonce(text, integer) TO service_role;

