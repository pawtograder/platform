-- 11. Touch updated_at triggers
-------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.lti_touch_updated_at()
RETURNS trigger LANGUAGE plpgsql SET search_path TO '' AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

CREATE TRIGGER "lti_platforms_touch" BEFORE UPDATE ON "public"."lti_platforms"
    FOR EACH ROW EXECUTE FUNCTION public.lti_touch_updated_at();
CREATE TRIGGER "lti_context_links_touch" BEFORE UPDATE ON "public"."lti_context_links"
    FOR EACH ROW EXECUTE FUNCTION public.lti_touch_updated_at();
CREATE TRIGGER "lti_line_items_touch" BEFORE UPDATE ON "public"."lti_line_items"
    FOR EACH ROW EXECUTE FUNCTION public.lti_touch_updated_at();
CREATE TRIGGER "lti_users_touch" BEFORE UPDATE ON "public"."lti_users"
    FOR EACH ROW EXECUTE FUNCTION public.lti_touch_updated_at();
