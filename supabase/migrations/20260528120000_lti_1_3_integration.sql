    "deployment_id" text NOT NULL,     -- https://purl.imsglobal.org/spec/lti/claim/deployment_id
    "name" text,
    CONSTRAINT "lti_deployments_unique" UNIQUE ("platform_id", "deployment_id")
);

ALTER TABLE "public"."lti_deployments" ENABLE ROW LEVEL SECURITY;
GRANT ALL ON TABLE "public"."lti_deployments" TO "authenticated";
GRANT ALL ON TABLE "public"."lti_deployments" TO "service_role";

CREATE POLICY "Admins manage lti_deployments" ON "public"."lti_deployments"
    FOR ALL USING (authorize_for_admin()) WITH CHECK (authorize_for_admin());
CREATE POLICY "Service role manages lti_deployments" ON "public"."lti_deployments"
    FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

-------------------------------------------------------------------------------
-- 3. Tool signing keys  (our keypairs; public half published as JWKS)
-------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS "public"."lti_tool_keys" (
    "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "created_at" timestamp with time zone DEFAULT now() NOT NULL,
    "kid" text NOT NULL UNIQUE,                 -- key id, surfaced in JWKS + JWT header
    "alg" text NOT NULL DEFAULT 'RS256',
    "public_jwk" jsonb NOT NULL,                -- published at /api/lti/jwks
    "private_key_pem_encrypted" text NOT NULL,  -- AES-GCM(base64) under LTI_KEY_ENCRYPTION_SECRET
    "retired_at" timestamp with time zone       -- when set, still published for verification but not used to sign
);

ALTER TABLE "public"."lti_tool_keys" ENABLE ROW LEVEL SECURITY;
GRANT ALL ON TABLE "public"."lti_tool_keys" TO "service_role";
-- Private keys never leave the server role. Authenticated users get nothing here;
-- the public JWKS is served by an endpoint using the service role.

CREATE POLICY "Service role manages lti_tool_keys" ON "public"."lti_tool_keys"
    FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

-------------------------------------------------------------------------------
-- 4. Context links  (LMS course/section -> Pawtograder class)
-------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS "public"."lti_context_links" (
    "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "created_at" timestamp with time zone DEFAULT now() NOT NULL,
    "updated_at" timestamp with time zone DEFAULT now() NOT NULL,
    "platform_id" bigint NOT NULL REFERENCES "public"."lti_platforms"("id") ON DELETE CASCADE,
    "deployment_id" text NOT NULL,
    "context_id" text NOT NULL,                 -- LTI context (course) id on the platform
    "context_label" text,
    "context_title" text,
    "class_id" bigint REFERENCES "public"."classes"("id") ON DELETE CASCADE,
    -- Service endpoints discovered from launch claims:
    "nrps_url" text,                            -- context_memberships_url (NRPS)
    "ags_lineitems_url" text,                   -- AGS lineitems collection endpoint
    "ags_scopes" text[] DEFAULT '{}'::text[],   -- granted AGS scopes from the launch
    "roster_sync_enabled" boolean DEFAULT false NOT NULL,
    "grade_sync_enabled" boolean DEFAULT false NOT NULL,
    "last_roster_sync_at" timestamp with time zone,
    "last_roster_sync_status" text,
    "last_roster_sync_message" text,
    CONSTRAINT "lti_context_links_unique" UNIQUE ("platform_id", "deployment_id", "context_id"),
    -- Tie each context link to a real deployment (launch upserts the deployment
    -- first), preventing orphaned/mismatched (platform_id, deployment_id) pairs.
    CONSTRAINT "lti_context_links_deployment_fkey"
      FOREIGN KEY ("platform_id", "deployment_id")
      REFERENCES "public"."lti_deployments" ("platform_id", "deployment_id") ON DELETE CASCADE
);

ALTER TABLE "public"."lti_context_links" ENABLE ROW LEVEL SECURITY;
-- The server reads nrps_url / ags_lineitems_url from this table and uses them as
-- outbound request targets with a bearer service token. Instructors must NOT be
-- able to repoint those (SSRF / token exfiltration), so grant them only the two
-- sync toggles; service_role (the launch/sync endpoints) manages the rest.
GRANT SELECT ON TABLE "public"."lti_context_links" TO "authenticated";
GRANT UPDATE ("roster_sync_enabled", "grade_sync_enabled") ON TABLE "public"."lti_context_links" TO "authenticated";
GRANT ALL ON TABLE "public"."lti_context_links" TO "service_role";

CREATE POLICY "Instructors read lti_context_links" ON "public"."lti_context_links"
    FOR SELECT USING (
        class_id IS NOT NULL AND public.authorizeforclassinstructor(class_id)
    );
CREATE POLICY "Instructors toggle lti_context_links sync" ON "public"."lti_context_links"
    FOR UPDATE USING (
        class_id IS NOT NULL AND public.authorizeforclassinstructor(class_id)
    ) WITH CHECK (
        class_id IS NOT NULL AND public.authorizeforclassinstructor(class_id)
    );
CREATE POLICY "Admins manage lti_context_links" ON "public"."lti_context_links"
    FOR ALL USING (authorize_for_admin()) WITH CHECK (authorize_for_admin());
CREATE POLICY "Service role manages lti_context_links" ON "public"."lti_context_links"
    FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

CREATE INDEX IF NOT EXISTS "lti_context_links_class_id_idx" ON "public"."lti_context_links" ("class_id");

