-- 10. Roster sync trigger (pg_cron -> Next.js /api/lti/sync-roster)
--
-- Mirrors the SIS hourly cron. The Next.js endpoint authenticates the call with
-- the LTI_CRON_SHARED_SECRET header. URL + secret are read from DB settings:
--   ALTER DATABASE postgres SET app.settings.app_url = 'https://app...';
--   ALTER DATABASE postgres SET app.settings.lti_cron_secret = '...';
-- If unset, the function no-ops so the migration is safe in all environments.
-------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.trigger_lti_roster_sync()
RETURNS void
LANGUAGE plpgsql SECURITY DEFINER SET search_path TO ''
AS $$
DECLARE
    v_url text := current_setting('app.settings.app_url', true);
    v_secret text := current_setting('app.settings.lti_cron_secret', true);
BEGIN
    IF v_url IS NULL OR v_url = '' THEN
        RAISE NOTICE 'LTI roster sync skipped: app.settings.app_url not configured';
        RETURN;
    END IF;

    -- Fire-and-forget POST; the endpoint enumerates roster_sync_enabled contexts.
    PERFORM net.http_post(
        url := v_url || '/api/lti/sync-roster',
        headers := jsonb_build_object(
            'Content-Type', 'application/json',
            'x-lti-cron-secret', COALESCE(v_secret, '')
        ),
        body := '{"all": true}'::jsonb
    );
EXCEPTION WHEN OTHERS THEN
    RAISE NOTICE 'LTI roster sync trigger failed: %', SQLERRM;
END;
$$;

REVOKE ALL ON FUNCTION public.trigger_lti_roster_sync() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.trigger_lti_roster_sync() TO service_role;
GRANT EXECUTE ON FUNCTION public.trigger_lti_roster_sync() TO postgres;

DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'pg_cron') THEN
        -- Run at :45 past the hour to avoid colliding with the SIS job (:15).
        PERFORM cron.schedule(
            'lti-roster-sync',
            '45 * * * *',
            'SELECT public.trigger_lti_roster_sync();'
        );
        RAISE NOTICE 'LTI roster sync cron scheduled hourly at :45';
    ELSE
        RAISE NOTICE 'pg_cron not available; skipping LTI roster sync schedule';
    END IF;
END;
$$;

