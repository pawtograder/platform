-- Create / upsert the exam row for an assignment (instructor only)
create or replace function public.exam_create(
  p_assignment_id bigint,
  p_source_type text default 'pdf',
  p_num_pages integer default 0,
  p_template_pdf_path text default null,
  p_template_markdown text default null
) returns bigint
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_class_id bigint;
  v_exam_id bigint;
begin
  select class_id into v_class_id from public.assignments where id = p_assignment_id;
  if v_class_id is null then
    raise exception 'Assignment % not found', p_assignment_id;
  end if;
  if auth.uid() is not null and not exists (
    select 1 from public.user_privileges up
    where up.user_id = auth.uid() and up.class_id = v_class_id and up.role = 'instructor'
  ) then
    raise exception 'Access denied: instructors only';
  end if;

  insert into public.exams (class_id, assignment_id, template_source_type, num_pages,
                            template_pdf_path, template_markdown)
  values (v_class_id, p_assignment_id, p_source_type, coalesce(p_num_pages,0),
          p_template_pdf_path, p_template_markdown)
  on conflict (assignment_id) do update
    set template_source_type = excluded.template_source_type,
        num_pages = excluded.num_pages,
        template_pdf_path = coalesce(excluded.template_pdf_path, exams.template_pdf_path),
        template_markdown = coalesce(excluded.template_markdown, exams.template_markdown)
  returning id into v_exam_id;

  return v_exam_id;
end;
$$;
revoke all on function public.exam_create(bigint, text, integer, text, text) from public;
grant execute on function public.exam_create(bigint, text, integer, text, text) to authenticated, service_role;

-- Replace the full question tree + regions transactionally.
-- p_questions: [{ client_id, parent_client_id, level, ordinal, label, prompt, answer_type, choices, points }]
-- p_regions:   [{ question_client_id|null, kind, page_number, x, y, width, height }]
create or replace function public.exam_upsert_questions_and_regions(
  p_exam_id bigint,
  p_questions jsonb,
  p_regions jsonb
) returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_class_id bigint;
  q jsonb;
  r jsonb;
  v_lvl int;
  v_new_id bigint;
  v_parent_id bigint;
begin
  select class_id into v_class_id from public.exams where id = p_exam_id;
  if v_class_id is null then
    raise exception 'Exam % not found', p_exam_id;
  end if;
  if auth.uid() is not null and not exists (
    select 1 from public.user_privileges up
    where up.user_id = auth.uid() and up.class_id = v_class_id and up.role = 'instructor'
  ) then
    raise exception 'Access denied: instructors only';
  end if;

  -- wipe existing structure (regions cascade from questions, but identity regions do not)
  delete from public.exam_question_regions where exam_id = p_exam_id;
  delete from public.exam_questions where exam_id = p_exam_id;

  -- pg_temp-qualified (the function runs with search_path = '')
  create temporary table pg_temp._exam_qmap (client_id text primary key, new_id bigint) on commit drop;

  -- insert questions level-by-level so parents exist before children
  for v_lvl in 1..3 loop
    for q in select value from jsonb_array_elements(coalesce(p_questions, '[]'::jsonb))
             where (value->>'level')::int = v_lvl
             order by coalesce((value->>'ordinal')::numeric, 0)
    loop
      v_parent_id := null;
      if q->>'parent_client_id' is not null then
        select new_id into v_parent_id from pg_temp._exam_qmap where client_id = q->>'parent_client_id';
      end if;
      insert into public.exam_questions
        (class_id, exam_id, parent_id, level, ordinal, label, prompt, answer_type, choices, points)
      values
        (v_class_id, p_exam_id, v_parent_id, v_lvl,
         coalesce((q->>'ordinal')::numeric, 0), q->>'label', q->>'prompt',
         q->>'answer_type', q->'choices',
         nullif(q->>'points','')::numeric)
      returning id into v_new_id;
      insert into pg_temp._exam_qmap(client_id, new_id) values (q->>'client_id', v_new_id);
    end loop;
  end loop;

  -- regions
  for r in select value from jsonb_array_elements(coalesce(p_regions, '[]'::jsonb))
  loop
    v_new_id := null;
    if r->>'question_client_id' is not null then
      select new_id into v_new_id from pg_temp._exam_qmap where client_id = r->>'question_client_id';
    end if;
    insert into public.exam_question_regions
      (class_id, exam_id, exam_question_id, kind, page_number, x, y, width, height)
    values
      (v_class_id, p_exam_id, v_new_id, coalesce(r->>'kind','answer'),
       (r->>'page_number')::int, (r->>'x')::numeric, (r->>'y')::numeric,
       (r->>'width')::numeric, (r->>'height')::numeric);
  end loop;
end;
$$;
revoke all on function public.exam_upsert_questions_and_regions(bigint, jsonb, jsonb) from public;
grant execute on function public.exam_upsert_questions_and_regions(bigint, jsonb, jsonb) to authenticated, service_role;

-- Scaffold / link rubric_parts -> rubric_criteria -> rubric_checks from the exam's
-- 3-level question tree, writing the back-reference {exam_question_id} into each
-- component's data jsonb. Idempotent (matches on data->>'exam_question_id').
create or replace function public.exam_sync_rubric_from_questions(
  p_exam_id bigint,
  p_rubric_id bigint
) returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_class_id bigint;
  v_assignment_id bigint;
  r1 record;  -- level 1 (part)
  r2 record;  -- level 2 (criteria)
  r3 record;  -- level 3 (check)
  v_part_id bigint;
  v_crit_id bigint;
begin
  select class_id, assignment_id into v_class_id, v_assignment_id
    from public.exams where id = p_exam_id;
  if v_class_id is null then
    raise exception 'Exam % not found', p_exam_id;
  end if;
  if auth.uid() is not null and not exists (
    select 1 from public.user_privileges up
    where up.user_id = auth.uid() and up.class_id = v_class_id and up.role = 'instructor'
  ) then
    raise exception 'Access denied: instructors only';
  end if;

  -- level 1 -> rubric_parts
  for r1 in select * from public.exam_questions where exam_id = p_exam_id and level = 1 order by ordinal loop
    select id into v_part_id from public.rubric_parts
      where rubric_id = p_rubric_id and (data->>'exam_question_id')::bigint = r1.id limit 1;
    if v_part_id is null then
      insert into public.rubric_parts (class_id, assignment_id, rubric_id, name, description, ordinal, data)
      values (v_class_id, v_assignment_id, p_rubric_id, coalesce(r1.label, 'Part'), r1.prompt, r1.ordinal,
              jsonb_build_object('exam_question_id', r1.id))
      returning id into v_part_id;
    else
      update public.rubric_parts set name = coalesce(r1.label, name), description = r1.prompt, ordinal = r1.ordinal
        where id = v_part_id;
    end if;

    -- level 2 -> rubric_criteria (children of r1)
    for r2 in select * from public.exam_questions
              where exam_id = p_exam_id and level = 2 and parent_id = r1.id order by ordinal loop
      select id into v_crit_id from public.rubric_criteria
        where rubric_id = p_rubric_id and (data->>'exam_question_id')::bigint = r2.id limit 1;
      if v_crit_id is null then
        insert into public.rubric_criteria
          (class_id, assignment_id, rubric_id, rubric_part_id, name, description, total_points, is_additive, ordinal, data)
        values (v_class_id, v_assignment_id, p_rubric_id, v_part_id, coalesce(r2.label,'Criteria'), r2.prompt,
                coalesce(r2.points, 0), true, r2.ordinal, jsonb_build_object('exam_question_id', r2.id))
        returning id into v_crit_id;
      else
        update public.rubric_criteria set rubric_part_id = v_part_id, name = coalesce(r2.label, name),
          description = r2.prompt, total_points = coalesce(r2.points, total_points), ordinal = r2.ordinal
          where id = v_crit_id;
      end if;

      -- level 3 -> rubric_checks (children of r2)
      for r3 in select * from public.exam_questions
                where exam_id = p_exam_id and level = 3 and parent_id = r2.id order by ordinal loop
        if not exists (
          select 1 from public.rubric_checks
          where rubric_criteria_id = v_crit_id and (data->>'exam_question_id')::bigint = r3.id
        ) then
          insert into public.rubric_checks
            (class_id, assignment_id, rubric_id, rubric_criteria_id, name, description, ordinal, points, is_annotation, is_comment_required, data)
          values (v_class_id, v_assignment_id, p_rubric_id, v_crit_id, coalesce(r3.label,'Check'), r3.prompt, r3.ordinal,
                  coalesce(r3.points, 0), false, false, jsonb_build_object('exam_question_id', r3.id));
        end if;
      end loop;
    end loop;
  end loop;
end;
$$;
revoke all on function public.exam_sync_rubric_from_questions(bigint, bigint) from public;
grant execute on function public.exam_sync_rubric_from_questions(bigint, bigint) to authenticated, service_role;

-- Create the submission for a confirmed scanned exam. Fires the existing
-- submissions_after_insert_hook (auto-creates the grading submission_review).
-- Called by the worker (service role) during finalize.
create or replace function public.exam_create_submission(p_scanned_submission_id bigint)
returns bigint
language plpgsql
security definer
-- Inserting into public.submissions fires the submission trigger chain (channels,
-- metrics, after-insert hook). Some of those triggers reference tables unqualified and
-- have no search_path of their own, so they inherit this function's. An empty search_path
-- makes them fail with "relation submissions does not exist" — keep public on the path.
set search_path = public, pg_temp
as $$
declare
  v_class_id bigint;
  v_assignment_id bigint;
  v_profile_id uuid;
  v_match_status text;
  v_existing bigint;
  v_submission_id bigint;
begin
  select ss.class_id, e.assignment_id, ss.matched_profile_id, ss.match_status, ss.submission_id
    into v_class_id, v_assignment_id, v_profile_id, v_match_status, v_existing
  from public.exam_scanned_submissions ss
  join public.exams e on e.id = ss.exam_id
  where ss.id = p_scanned_submission_id;

  if v_class_id is null then
    raise exception 'Scanned submission % not found', p_scanned_submission_id;
  end if;
  if v_existing is not null then
    return v_existing; -- idempotent
  end if;
  if v_match_status <> 'confirmed' or v_profile_id is null then
    raise exception 'Scanned submission % is not a confirmed match', p_scanned_submission_id;
  end if;

  insert into public.submissions
    (assignment_id, profile_id, class_id, sha, repository, run_attempt, run_number, is_active)
  values
    (v_assignment_id, v_profile_id, v_class_id, 'exam',
     'exam-scan/' || p_scanned_submission_id::text, 0, 1, true)
  returning id into v_submission_id;

  update public.exam_scanned_submissions set submission_id = v_submission_id
    where id = p_scanned_submission_id;

  return v_submission_id;
end;
$$;
revoke all on function public.exam_create_submission(bigint) from public;
grant execute on function public.exam_create_submission(bigint) to service_role;

