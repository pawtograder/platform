create table if not exists public.exams (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  assignment_id bigint not null references public.assignments(id) on delete cascade,
  template_source_type text not null default 'pdf' check (template_source_type in ('pdf','markdown')),
  template_pdf_path text,
  template_markdown text,
  num_pages integer not null default 0,
  status text not null default 'draft' check (status in ('draft','ready')),
  created_by uuid default auth.uid(),
  constraint exams_assignment_id_unique unique (assignment_id)
);

create table if not exists public.exam_template_pages (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  page_number integer not null,
  image_path text not null,
  width integer not null,
  height integer not null
);

create table if not exists public.exam_questions (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  parent_id bigint references public.exam_questions(id) on delete cascade,
  level smallint not null check (level between 1 and 3),
  ordinal numeric not null default 0,
  label text,
  prompt text,
  answer_type text check (answer_type in
    ('multiple_choice','free_text','numeric','true_false','short_answer')),
  choices jsonb,
  points numeric
);

create table if not exists public.exam_question_regions (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  exam_question_id bigint references public.exam_questions(id) on delete cascade,
  kind text not null default 'answer' check (kind in ('answer','student_id','name')),
  page_number integer not null,
  x numeric not null,
  y numeric not null,
  width numeric not null,
  height numeric not null,
  -- identity regions (name / student_id) are not tied to a question
  constraint exam_region_identity_has_no_question
    check (kind = 'answer' or exam_question_id is null)
);

create table if not exists public.exam_scan_batches (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  uploaded_by uuid default auth.uid(),
  original_pdf_path text,
  total_pages integer not null default 0,
  pages_per_exam integer not null default 1,
  status text not null default 'uploaded'
    check (status in ('uploaded','ocr','matching','review','finalizing','completed','error')),
  error text
);

create table if not exists public.exam_scanned_submissions (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  batch_id bigint not null references public.exam_scan_batches(id) on delete cascade,
  exam_index integer not null,
  detected_name text,
  detected_sis_id text,
  matched_profile_id uuid references public.profiles(id) on delete set null,
  match_confidence numeric,
  match_status text not null default 'unmatched'
    check (match_status in ('unmatched','suggested','confirmed','skipped')),
  extracted jsonb,
  submission_id bigint references public.submissions(id) on delete set null,
  -- Single completion marker for this scanned submission's finalize. Set (transactionally
  -- at the end of finalize) only once the submission, its page files, and the exam artifact
  -- are all written. NULL = not finalized (never started OR crashed mid-finalize), which is
  -- exactly the set enqueue_exam_finalize re-enqueues and maybeCompleteBatch waits on.
  finalized_at timestamptz
);

create table if not exists public.exam_scan_pages (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  class_id bigint not null references public.classes(id) on delete cascade,
  exam_id bigint not null references public.exams(id) on delete cascade,
  batch_id bigint not null references public.exam_scan_batches(id) on delete cascade,
  page_index integer not null,
