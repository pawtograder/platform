create or replace function public.enqueue_exam_process_batch(p_batch_id bigint)
returns integer
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_class_id bigint;
  v_count integer := 0;
  rec record;
begin
  select class_id into v_class_id from public.exam_scan_batches where id = p_batch_id;
  if v_class_id is null then
    raise exception 'Batch % not found', p_batch_id;
  end if;
  if auth.uid() is not null and not exists (
    select 1 from public.user_privileges up
    where up.user_id = auth.uid() and up.class_id = v_class_id and up.role in ('instructor','grader')
  ) then
    raise exception 'Access denied: staff only';
  end if;

  update public.exam_scan_batches set status = 'ocr', error = null where id = p_batch_id;

  -- one OCR job per scan page
  for rec in select id from public.exam_scan_pages where batch_id = p_batch_id order by page_index loop
    perform pgmq_public.send('exam_processing', jsonb_build_object(
      'method', 'process_page', 'class_id', v_class_id, 'batch_id', p_batch_id,
      'args', jsonb_build_object('scan_page_id', rec.id)));
    v_count := v_count + 1;
  end loop;

  -- one match job for the batch (groups pages into scanned submissions, reads identity)
  perform pgmq_public.send('exam_processing', jsonb_build_object(
    'method', 'match', 'class_id', v_class_id, 'batch_id', p_batch_id,
    'args', jsonb_build_object('batch_id', p_batch_id)));

  -- best-effort immediate kick; the cron invoker drains the queue regardless
  begin
    perform public.call_edge_function_internal(
      '/functions/v1/exam-async-worker', 'POST',
      '{"Content-type":"application/json","x-supabase-webhook-source":"exam-async-worker"}'::jsonb,
      '{}'::jsonb, 3000, null, null, null, null, null);
  exception when others then null;
  end;

  return v_count;
end;
$$;
revoke all on function public.enqueue_exam_process_batch(bigint) from public;
grant execute on function public.enqueue_exam_process_batch(bigint) to authenticated, service_role;

create or replace function public.enqueue_exam_finalize(p_batch_id bigint)
returns integer
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_class_id bigint;
  v_count integer := 0;
  rec record;
begin
  select class_id into v_class_id from public.exam_scan_batches where id = p_batch_id;
  if v_class_id is null then
    raise exception 'Batch % not found', p_batch_id;
  end if;
  if auth.uid() is not null and not exists (
    select 1 from public.user_privileges up
    where up.user_id = auth.uid() and up.class_id = v_class_id and up.role in ('instructor','grader')
  ) then
    raise exception 'Access denied: staff only';
  end if;

  -- Re-enqueue every confirmed submission that is not yet finalized. finalized_at is the
  -- crash-safety signal: a submission whose finalize crashed before completing (finalized_at
  -- still NULL, whether or not a partial submission/artifact exists) is picked back up here,
  -- as is one that never started.
  for rec in select ss.id from public.exam_scanned_submissions ss
             where ss.batch_id = p_batch_id and ss.match_status = 'confirmed'
               and ss.finalized_at is null loop
    perform pgmq_public.send('exam_processing', jsonb_build_object(
      'method', 'finalize', 'class_id', v_class_id, 'batch_id', p_batch_id,
      'args', jsonb_build_object('scanned_submission_id', rec.id)));
    v_count := v_count + 1;
  end loop;

  -- Only flip into 'finalizing' when there is actually work; otherwise leave the batch's
  -- status untouched (e.g. an already-'completed' batch stays completed) so a redundant
  -- finalize click can't strand it.
  if v_count > 0 then
    update public.exam_scan_batches set status = 'finalizing', error = null where id = p_batch_id;

    -- best-effort immediate kick; the cron invoker drains the queue regardless
    begin
      perform public.call_edge_function_internal(
        '/functions/v1/exam-async-worker', 'POST',
        '{"Content-type":"application/json","x-supabase-webhook-source":"exam-async-worker"}'::jsonb,
        '{}'::jsonb, 3000, null, null, null, null, null);
    exception when others then null;
    end;
  end if;

  return v_count;
end;
$$;
revoke all on function public.enqueue_exam_finalize(bigint) from public;
grant execute on function public.enqueue_exam_finalize(bigint) to authenticated, service_role;

-- Background invoker (drains the exam_processing queue via the edge worker)
create or replace function public.invoke_exam_async_worker_background_task()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  message_count integer;
  worker_count integer;
  i integer;
begin
  select count(*)::integer into message_count from pgmq_public.read('exam_processing', 0, 20);
  if message_count = 0 then
    return;
  elsif message_count >= 10 then
    worker_count := 5;
  else
    worker_count := 2;
  end if;
  for i in 1..worker_count loop
    perform public.call_edge_function_internal(
      '/functions/v1/exam-async-worker', 'POST',
      '{"Content-type":"application/json","x-supabase-webhook-source":"exam-async-worker"}'::jsonb,
      '{}'::jsonb, 3000, null, null, null, null, null);
  end loop;
end;
$$;
revoke all on function public.invoke_exam_async_worker_background_task() from public;
grant execute on function public.invoke_exam_async_worker_background_task() to service_role;

select cron.unschedule('invoke-exam-async-worker-every-minute')
where exists (select 1 from cron.job where jobname = 'invoke-exam-async-worker-every-minute');
select cron.schedule(
  'invoke-exam-async-worker-every-minute',
  '* * * * *',
  $$select public.invoke_exam_async_worker_background_task();$$
);
