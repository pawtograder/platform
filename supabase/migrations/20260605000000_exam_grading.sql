-- Exam Grading + OCR System
-- Adds exam-mode assignments: template (PDF/markdown) with a 3-level question
-- structure + bounding-box regions, scanned-exam batches that are OCR'd and matched
-- to students via an async pgmq worker, and per-student submissions whose raw pages
-- are submission_files and whose OCR result is one submission_artifacts JSON blob.
--
-- Conventions used here:
--   * RLS = inline EXISTS on public.user_privileges (no authorizeforclass* helpers).
--   * Async processing = pgmq queue + cron invoker + edge function (like github-async-worker).
--   * Deliberate, minimal indexes (rely on the user_privileges PK for the RLS path).

-- ---------------------------------------------------------------------------
-- 1) Queue + DLQ
-- ---------------------------------------------------------------------------
do $$ begin
  perform pgmq.create('exam_processing');
exception when others then null;
end $$;

do $$ begin
  perform pgmq.create('exam_processing_dlq');
exception when others then null;
end $$;

