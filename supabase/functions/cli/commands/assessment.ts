/**
 * Assessment export — streams a privacy-controlled snapshot of rubrics, final
 * grading review scores, autograder test results, hint feedback, and
 * gradebook columns for a class.
 *
 * Architecture (see /docs design discussion):
 *   - One CLI run = one random salt held only by the CLI.
 *   - The CLI fans out parallel calls keyed on assignment_slug. Each call
 *     constructs its own tokenizer using the same salt, so tokens for the
 *     same raw id agree across calls and can be joined on the analyst side.
 *   - First call: assessment.export.preamble emits manifest, students, and
 *     sections (the thin slice this file currently implements).
 *   - Subsequent calls (future tasks): assessment.export.assignment streams
 *     per-assignment rubric, autograder, scores, tests, hints; and
 *     assessment.export.gradebook streams gradebook columns + private cells.
 *
 * Identity modes:
 *   - "raw"    — emit real ids/emails/names. Requires confirm_pii: true.
 *   - "hash"   — HMAC of id with caller-supplied salt; joinable across runs.
 *   - "opaque" — HMAC with caller-generated random salt; intra-run only.
 */

import type { MCPAuthContext } from "../../_shared/MCPAuth.ts";
import { registerCommand } from "../router.ts";
import { CLICommandError } from "../errors.ts";
import { getAdminClient } from "../utils/supabase.ts";
import { resolveClass } from "../utils/resolvers.ts";
import { assertUserCanAccessClass } from "../utils/auth.ts";
import { streamNdjson } from "../utils/ndjson.ts";
import { fetchRubricWithHierarchy } from "../utils/rubric.ts";
import { resolveSelectors } from "../utils/selectors.ts";
import { createTokenizer, type IdentityMode, type Tokenizer } from "../utils/tokenization.ts";

const SCHEMA_VERSION = 1;
const STUDENT_PAGE_SIZE = 500;
const FACT_PAGE_SIZE = 1000;
const DEFAULT_TEST_OUTPUT_MAX_BYTES = 4096;

interface PreambleParams {
  class?: string | number;
  identity_mode?: IdentityMode;
  salt?: string;
  /** Required when identity_mode === "raw" — explicit acknowledgement. */
  confirm_pii?: boolean;
  /** UUID generated by the CLI per run. Echoed in the manifest for traceability. */
  dump_id?: string;
  /**
   * Assignment selectors. Each entry is one of:
   *   - numeric id (string of digits or number)
   *   - exact slug
   *   - minimatch-style glob on slug (e.g. "hw-*", "lab?")
   * Null/empty array => all assignments in the class.
   */
  assignments?: Array<string | number>;
  /** Gradebook column selectors, same id|slug|glob shape. Empty => all columns. */
  gradebook_columns?: Array<string | number>;
}

async function handlePreamble(ctx: MCPAuthContext, rawParams: Record<string, unknown>): Promise<Response> {
  const params = rawParams as unknown as PreambleParams;
  const classIdentifier = params.class;
  const mode: IdentityMode = params.identity_mode ?? "opaque";
  const dumpId = params.dump_id ?? crypto.randomUUID();

  if (!classIdentifier) throw new CLICommandError("class is required");
  if (mode !== "raw" && mode !== "hash" && mode !== "opaque") {
    throw new CLICommandError(`invalid identity_mode: ${String(mode)}`);
  }
  if (mode === "raw" && params.confirm_pii !== true) {
    throw new CLICommandError(
      "identity_mode=raw exposes real student ids, emails, and names. Re-run with confirm_pii: true to acknowledge.",
      400
    );
  }
  if (mode === "hash" || mode === "opaque") {
    if (!params.salt || typeof params.salt !== "string") {
      throw new CLICommandError(`identity_mode=${mode} requires a salt`);
    }
    // Mirror createTokenizer's contract here so a too-short salt surfaces as
    // a 400 validation error instead of a generic 500 from inside the
    // tokenizer constructor.
    if (params.salt.length < 16) {
      throw new CLICommandError(`identity_mode=${mode} requires a salt of at least 16 characters`);
    }
  }

  const supabase = getAdminClient();
  const classData = await resolveClass(supabase, classIdentifier);
  await assertUserCanAccessClass(supabase, ctx.userId, classData.id);

  const tokenizer = mode === "raw" ? null : await createTokenizer(params.salt!);

  return streamNdjson(async (writer) => {
    await writer.write({
      kind: "manifest",
      schema_version: SCHEMA_VERSION,
      dump_id: dumpId,
      identity_mode: mode,
      exported_at: new Date().toISOString(),
      class: { id: classData.id, slug: classData.slug, name: classData.name }
    });

    const subjectCount = await streamStudents(supabase, classData.id, mode, tokenizer, writer);
    const sectionCount = await streamSections(supabase, classData.id, mode, tokenizer, writer);
    const assignmentCount = await streamAssignments(supabase, classData.id, params.assignments, writer);
    const gradebookColumnCount = await streamGradebookColumns(supabase, classData.id, params.gradebook_columns, writer);

    await writer.write({
      kind: "end",
      counts: {
        subjects: subjectCount,
        sections: sectionCount,
        assignments: assignmentCount,
        gradebook_columns: gradebookColumnCount
      }
    });
  });
}

/**
 * Page through students in the class and emit one {kind:"subject"} record per
 * student. Pagination is by private_profile_id, NOT OFFSET, so a class with
 * 1000 students still completes in O(1) DB cursors regardless of page size.
 *
 * The subject token is derived from private_profile_id — this is the foreign
 * key used by every per-assignment fact table (submissions.profile_id,
 * grader_result_tests.student_id, submission_*_comments.target_student_profile_id,
 * gradebook_column_students.student_id, assignment_groups_members.profile_id).
 * Using user_id would mean the analyst couldn't join subjects to fact rows.
 */
async function streamStudents(
  supabase: ReturnType<typeof getAdminClient>,
  classId: number,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  let cursor: string | null = null;
  let total = 0;

  while (true) {
    let query = supabase
      .from("user_roles")
      .select("user_id, class_section_id, lab_section_id, private_profile_id")
      .eq("class_id", classId)
      .eq("disabled", false)
      .eq("role", "student")
      .order("private_profile_id", { ascending: true })
      .limit(STUDENT_PAGE_SIZE);

    if (cursor !== null) query = query.gt("private_profile_id", cursor);

    const { data: rows, error } = await query;
    if (error) throw new CLICommandError(`Failed to load students: ${error.message}`, 500);
    if (!rows || rows.length === 0) break;

    // For raw mode, join users to surface email/name. We do this per-page in
    // a single roundtrip rather than nesting in the user_roles select to keep
    // the query plan simple and avoid PostgREST's nested-select overhead.
    let userInfo: Map<string, { email: string | null; name: string | null }> | null = null;
    if (mode === "raw") {
      const userIds = rows.map((r) => r.user_id);
      const { data: userRows, error: userErr } = await supabase
        .from("users")
        .select("user_id, email, name")
        .in("user_id", userIds);
      if (userErr) throw new CLICommandError(`Failed to load user emails: ${userErr.message}`, 500);
      userInfo = new Map((userRows ?? []).map((u) => [u.user_id, { email: u.email, name: u.name }]));
    }

    for (const row of rows) {
      const subjectRef =
        tokenizer === null
          ? { id: row.private_profile_id }
          : { token: await tokenizer.token("subject", row.private_profile_id) };

      const classSectionRef =
        row.class_section_id === null
          ? null
          : tokenizer === null
            ? { id: row.class_section_id }
            : { token: await tokenizer.token("section_class", row.class_section_id) };

      const labSectionRef =
        row.lab_section_id === null
          ? null
          : tokenizer === null
            ? { id: row.lab_section_id }
            : { token: await tokenizer.token("section_lab", row.lab_section_id) };

      const record: Record<string, unknown> = {
        kind: "subject",
        subject_kind: "student",
        ...subjectRef,
        class_section: classSectionRef,
        lab_section: labSectionRef
      };

      if (mode === "raw") {
        const info = userInfo!.get(row.user_id);
        // user_id is included alongside private_profile_id ("id") in raw mode
        // because it's the only id stable across classes — useful for analysts
        // joining multiple class exports for the same student.
        record.user_id = row.user_id;
        record.email = info?.email ?? null;
        record.name = info?.name ?? null;
      }

      await writer.write(record);
      total += 1;
    }

    if (rows.length < STUDENT_PAGE_SIZE) break;
    cursor = rows[rows.length - 1]!.private_profile_id;
  }

  return total;
}

/**
 * Class and lab sections in the class. Section names are passed through
 * verbatim — they are usually what instructors want for analysis. The risk
 * (section names can re-identify students in small labs) is documented in
 * the manifest and surfaced to the CLI user; this function does not redact.
 */
async function streamSections(
  supabase: ReturnType<typeof getAdminClient>,
  classId: number,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  let total = 0;

  const { data: classSections, error: csErr } = await supabase
    .from("class_sections")
    .select("id, name, meeting_times, meeting_location, campus")
    .eq("class_id", classId)
    .order("id", { ascending: true });
  if (csErr) throw new CLICommandError(`Failed to load class sections: ${csErr.message}`, 500);

  for (const sec of classSections ?? []) {
    const ref = tokenizer === null ? { id: sec.id } : { token: await tokenizer.token("section_class", sec.id) };
    await writer.write({
      kind: "section",
      section_kind: "class",
      ...ref,
      name: sec.name,
      meeting_times: sec.meeting_times,
      meeting_location: sec.meeting_location,
      campus: sec.campus
    });
    total += 1;
  }

  const { data: labSections, error: lsErr } = await supabase
    .from("lab_sections")
    .select("id, name, meeting_times, meeting_location, campus, day_of_week, start_time, end_time")
    .eq("class_id", classId)
    .order("id", { ascending: true });
  if (lsErr) throw new CLICommandError(`Failed to load lab sections: ${lsErr.message}`, 500);

  for (const sec of labSections ?? []) {
    const ref = tokenizer === null ? { id: sec.id } : { token: await tokenizer.token("section_lab", sec.id) };
    await writer.write({
      kind: "section",
      section_kind: "lab",
      ...ref,
      name: sec.name,
      meeting_times: sec.meeting_times,
      meeting_location: sec.meeting_location,
      campus: sec.campus,
      day_of_week: sec.day_of_week,
      start_time: sec.start_time,
      end_time: sec.end_time
    });
    total += 1;
  }

  return total;
}

/**
 * Resolve assignment selectors against the class and emit one
 * {kind:"assignment"} record per match. Assignment ids/slugs are NOT
 * tokenized — they are the analyst's primary key for joining per-assignment
 * fact rows to the catalog, and the slug is course design metadata, not PII.
 *
 * Unmatched selectors are emitted as a single {kind:"warning"} record so the
 * CLI can surface them; the stream does not fail.
 */
async function streamAssignments(
  supabase: ReturnType<typeof getAdminClient>,
  classId: number,
  selectors: PreambleParams["assignments"],
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  const { data: rows, error } = await supabase
    .from("assignments")
    .select(
      "id, slug, title, group_config, due_date, release_date, total_points, has_autograder, has_handgrader, grading_rubric_id"
    )
    .eq("class_id", classId)
    .order("id", { ascending: true });
  if (error) throw new CLICommandError(`Failed to load assignments: ${error.message}`, 500);

  const candidates = (rows ?? []).map((r) => ({ ...r, slug: r.slug ?? null }));
  const { resolved, unmatched } = resolveSelectors(selectors, candidates);

  if (unmatched.length > 0) {
    await writer.write({
      kind: "warning",
      scope: "assignments",
      message: `${unmatched.length} assignment selector(s) matched nothing`,
      selectors: unmatched
    });
  }

  for (const a of resolved) {
    await writer.write({
      kind: "assignment",
      id: a.id,
      slug: a.slug,
      title: a.title,
      group_config: a.group_config,
      due_date: a.due_date,
      release_date: a.release_date,
      total_points: a.total_points,
      has_autograder: a.has_autograder,
      has_handgrader: a.has_handgrader,
      grading_rubric_id: a.grading_rubric_id
    });
  }
  return resolved.length;
}

/**
 * Resolve gradebook column selectors. instructor_only columns are always
 * included (this export is for instructor analysis). The accompanying
 * gradebook_score rows are filtered server-side to is_private = true; that
 * filter is owned by the per-assignment gradebook stream, not here.
 */
async function streamGradebookColumns(
  supabase: ReturnType<typeof getAdminClient>,
  classId: number,
  selectors: PreambleParams["gradebook_columns"],
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  const { data: rows, error } = await supabase
    .from("gradebook_columns")
    .select(
      "id, slug, name, description, max_score, instructor_only, released, sort_order, score_expression, render_expression, dependencies"
    )
    .eq("class_id", classId)
    .order("sort_order", { ascending: true, nullsFirst: false })
    .order("id", { ascending: true });
  if (error) throw new CLICommandError(`Failed to load gradebook columns: ${error.message}`, 500);

  const candidates = (rows ?? []).map((r) => ({ ...r, slug: r.slug }));
  const { resolved, unmatched } = resolveSelectors(selectors, candidates);

  if (unmatched.length > 0) {
    await writer.write({
      kind: "warning",
      scope: "gradebook_columns",
      message: `${unmatched.length} gradebook column selector(s) matched nothing`,
      selectors: unmatched
    });
  }

  for (const c of resolved) {
    await writer.write({
      kind: "gradebook_column",
      id: c.id,
      slug: c.slug,
      name: c.name,
      description: c.description,
      max_score: c.max_score,
      instructor_only: c.instructor_only,
      released: c.released,
      sort_order: c.sort_order,
      score_expression: c.score_expression,
      render_expression: c.render_expression,
      dependencies: c.dependencies
    });
  }
  return resolved.length;
}

interface AssignmentExportParams {
  class?: string | number;
  identity_mode?: IdentityMode;
  salt?: string;
  confirm_pii?: boolean;
  /** Single assignment id or slug. The CLI resolves selectors via preamble and fans out one call per id. */
  assignment?: string | number;
  /** Include grader_result_tests.output (truncated to test_output_max_bytes). */
  with_test_output?: boolean;
  test_output_max_bytes?: number;
}

/**
 * Streams everything specific to one assignment: rubric tree, autograder
 * config, groups (if applicable), is_active submissions, rubric check
 * results from the final grading review, autograder test results, and hint
 * feedback. The CLI calls this once per assignment, optionally in parallel,
 * passing the same salt so subject/group/submission tokens line up across
 * calls.
 */
async function handleAssignmentExport(ctx: MCPAuthContext, rawParams: Record<string, unknown>): Promise<Response> {
  const params = rawParams as unknown as AssignmentExportParams;
  const classIdentifier = params.class;
  const assignmentIdentifier = params.assignment;
  const mode: IdentityMode = params.identity_mode ?? "opaque";
  const withTestOutput = params.with_test_output === true;
  const testOutputMax = Math.max(
    0,
    Math.min(1024 * 1024, params.test_output_max_bytes ?? DEFAULT_TEST_OUTPUT_MAX_BYTES)
  );

  if (!classIdentifier) throw new CLICommandError("class is required");
  if (!assignmentIdentifier) throw new CLICommandError("assignment is required");
  if (mode === "raw" && params.confirm_pii !== true) {
    throw new CLICommandError(
      "identity_mode=raw exposes real student ids. Re-run with confirm_pii: true to acknowledge.",
      400
    );
  }
  if (mode === "hash" || mode === "opaque") {
    if (!params.salt || typeof params.salt !== "string") {
      throw new CLICommandError(`identity_mode=${mode} requires a salt`);
    }
    if (params.salt.length < 16) {
      throw new CLICommandError(`identity_mode=${mode} requires a salt of at least 16 characters`);
    }
  }

  const supabase = getAdminClient();
  const classData = await resolveClass(supabase, classIdentifier);
  await assertUserCanAccessClass(supabase, ctx.userId, classData.id);

  // Resolve the assignment by id or exact slug. Globs would have already been
  // expanded by the preamble call; per-assignment streams take a concrete id.
  const assignmentRow = await resolveOneAssignment(supabase, classData.id, assignmentIdentifier);

  const tokenizer = mode === "raw" ? null : await createTokenizer(params.salt!);

  return streamNdjson(async (writer) => {
    await writer.write({
      kind: "manifest",
      schema_version: SCHEMA_VERSION,
      identity_mode: mode,
      class: { id: classData.id, slug: classData.slug },
      assignment: { id: assignmentRow.id, slug: assignmentRow.slug, title: assignmentRow.title }
    });

    const rubricCount = await streamRubric(supabase, assignmentRow.grading_rubric_id, mode, tokenizer, writer);
    const autograderTestCount = await streamAutograderConfig(supabase, assignmentRow.id, writer);
    const groupCount = await streamGroups(supabase, assignmentRow.id, classData.id, mode, tokenizer, writer);

    // Submissions establish (submission_id → submission_token) and the subject
    // each row belongs to. Subsequent fact streams reuse the same tokenizer
    // so all references to the same submission produce the same token.
    const { submissionCount, submissionIds } = await streamSubmissions(
      supabase,
      assignmentRow.id,
      mode,
      tokenizer,
      writer
    );

    const scoreCount = await streamScores(supabase, submissionIds, mode, tokenizer, writer);
    const { testCount, graderTestIds } = await streamGraderTests(
      supabase,
      submissionIds,
      mode,
      tokenizer,
      writer,
      withTestOutput,
      testOutputMax
    );
    const hintCount = await streamHints(supabase, graderTestIds, mode, tokenizer, writer);
    const errorPinEngagementCount = await streamErrorPinEngagement(
      supabase,
      classData.id,
      submissionIds,
      mode,
      tokenizer,
      writer
    );

    await writer.write({
      kind: "end",
      counts: {
        rubric_checks: rubricCount,
        autograder_tests: autograderTestCount,
        groups: groupCount,
        submissions: submissionCount,
        scores: scoreCount,
        grader_tests: testCount,
        hints: hintCount,
        error_pin_engagement: errorPinEngagementCount
      }
    });
  });
}

async function resolveOneAssignment(
  supabase: ReturnType<typeof getAdminClient>,
  classId: number,
  identifier: string | number
) {
  const idStr = String(identifier);
  if (/^\d+$/.test(idStr)) {
    const { data } = await supabase
      .from("assignments")
      .select("id, slug, title, grading_rubric_id, group_config")
      .eq("class_id", classId)
      .eq("id", Number(idStr))
      .maybeSingle();
    if (data) return data;
  }
  const { data } = await supabase
    .from("assignments")
    .select("id, slug, title, grading_rubric_id, group_config")
    .eq("class_id", classId)
    .eq("slug", idStr)
    .maybeSingle();
  if (!data) throw new CLICommandError(`assignment not found in class ${classId}: ${identifier}`, 404);
  return data;
}

/**
 * Emit the rubric tree as a flat sequence of part/criteria/check records.
 * Each record carries the parent ids so analysts can rebuild the hierarchy.
 * rubric_check_token comes from rubric_check.id; check ids are not PII (they
 * are course design data) but the analyst will join score rows on the token.
 */
async function streamRubric(
  supabase: ReturnType<typeof getAdminClient>,
  rubricId: number | null,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  if (rubricId === null) return 0;
  const rubric = await fetchRubricWithHierarchy(supabase, rubricId);
  if (!rubric) return 0;

  await writer.write({
    kind: "rubric",
    id: rubric.id,
    name: rubric.name,
    description: rubric.description,
    cap_score_to_assignment_points: rubric.cap_score_to_assignment_points,
    review_round: rubric.review_round
  });

  let checkCount = 0;
  for (const part of rubric.rubric_parts ?? []) {
    await writer.write({
      kind: "rubric_part",
      id: part.id,
      rubric_id: rubric.id,
      name: part.name,
      description: part.description,
      ordinal: part.ordinal
    });
    for (const criteria of part.rubric_criteria ?? []) {
      await writer.write({
        kind: "rubric_criteria",
        id: criteria.id,
        rubric_part_id: part.id,
        name: criteria.name,
        description: criteria.description,
        ordinal: criteria.ordinal,
        total_points: criteria.total_points,
        is_additive: criteria.is_additive,
        is_deduction_only: criteria.is_deduction_only,
        min_checks_per_submission: criteria.min_checks_per_submission,
        max_checks_per_submission: criteria.max_checks_per_submission
      });
      for (const check of criteria.rubric_checks ?? []) {
        const ref = tokenizer === null ? { id: check.id } : { token: await tokenizer.token("rubric_check", check.id) };
        await writer.write({
          kind: "rubric_check",
          ...ref,
          rubric_criteria_id: criteria.id,
          name: check.name,
          description: check.description,
          ordinal: check.ordinal,
          points: check.points,
          is_annotation: check.is_annotation,
          is_required: check.is_required,
          student_visibility: check.student_visibility
        });
        checkCount += 1;
      }
    }
  }
  return checkCount;
}

/**
 * Emit the autograder test catalog from autograder.config. The catalog is
 * the source of truth for "what tests exist" — tests no student triggered
 * still appear here. grader_result_tests rows reference (name, part) since
 * the autograder doesn't assign stable test ids.
 */
async function streamAutograderConfig(
  supabase: ReturnType<typeof getAdminClient>,
  assignmentId: number,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  const { data, error } = await supabase
    .from("autograder")
    .select(
      "id, config, grader_repo, grader_commit_sha, latest_autograder_sha, max_submissions_count, max_submissions_period_secs"
    )
    .eq("id", assignmentId)
    .maybeSingle();
  if (error) throw new CLICommandError(`Failed to load autograder config: ${error.message}`, 500);
  if (!data) return 0;

  await writer.write({
    kind: "autograder",
    grader_repo: data.grader_repo,
    grader_commit_sha: data.grader_commit_sha,
    latest_autograder_sha: data.latest_autograder_sha,
    max_submissions_count: data.max_submissions_count,
    max_submissions_period_secs: data.max_submissions_period_secs
  });

  // The autograder config schema lives in PawtograderYml.d.ts but its shape
  // varies across versions. We pass through the test catalog verbatim if it
  // looks like a list of test specs; otherwise we just emit the raw config
  // so analysts can introspect and we don't lose data on schema drift.
  const config = data.config as Record<string, unknown> | null;
  if (config && Array.isArray((config as { tests?: unknown }).tests)) {
    const tests = (config as { tests: Array<Record<string, unknown>> }).tests;
    for (const t of tests) {
      await writer.write({
        kind: "autograder_test",
        name: t.name ?? null,
        part: t.part ?? null,
        max_score: t.max_score ?? null,
        is_released: t.is_released ?? null,
        // Pass through the rest so analysts have the full spec. Excluded from
        // the named columns above to keep this stable as the schema evolves.
        spec: t
      });
    }
    return tests.length;
  }
  await writer.write({ kind: "autograder_raw_config", config });
  return 0;
}

/**
 * Emit assignment_groups + members for group assignments. Group token is
 * derived from group.id; member references use subject tokens so analysts
 * can join groups → group members → subjects without a separate lookup.
 */
async function streamGroups(
  supabase: ReturnType<typeof getAdminClient>,
  assignmentId: number,
  classId: number,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  const { data: groups, error: gErr } = await supabase
    .from("assignment_groups")
    .select("id, name")
    .eq("assignment_id", assignmentId)
    .eq("class_id", classId)
    .order("id", { ascending: true });
  if (gErr) throw new CLICommandError(`Failed to load groups: ${gErr.message}`, 500);
  if (!groups || groups.length === 0) return 0;

  const groupIds = groups.map((g) => g.id);
  const { data: members, error: mErr } = await supabase
    .from("assignment_groups_members")
    .select("assignment_group_id, profile_id")
    .in("assignment_group_id", groupIds);
  if (mErr) throw new CLICommandError(`Failed to load group members: ${mErr.message}`, 500);

  const membersByGroup = new Map<number, string[]>();
  for (const m of members ?? []) {
    const list = membersByGroup.get(m.assignment_group_id) ?? [];
    list.push(m.profile_id);
    membersByGroup.set(m.assignment_group_id, list);
  }

  for (const g of groups) {
    const ref = tokenizer === null ? { id: g.id } : { token: await tokenizer.token("group", g.id) };
    const memberRefs = await Promise.all(
      (membersByGroup.get(g.id) ?? []).map(async (pid) =>
        tokenizer === null ? { id: pid } : { token: await tokenizer.token("subject", pid) }
      )
    );
    await writer.write({
      kind: "group",
      ...ref,
      // Group names are usually instructor-assigned ("Group 7") not PII; keep
      // them in raw form. If a course uses identifying group names this is
      // documented in the manifest's identity-mode notes for the analyst.
      name: g.name,
      members: memberRefs
    });
  }
  return groups.length;
}

/**
 * Page through is_active submissions for the assignment and emit one record
 * per submission. Returns the list of submission ids so subsequent fact
 * streams can scope their queries; this gives us a single authoritative
 * filter (is_active=true) instead of repeating the join in each fact query.
 */
async function streamSubmissions(
  supabase: ReturnType<typeof getAdminClient>,
  assignmentId: number,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<{ submissionCount: number; submissionIds: number[] }> {
  let cursor = 0;
  let total = 0;
  const ids: number[] = [];

  while (true) {
    const { data: rows, error } = await supabase
      .from("submissions")
      .select(
        "id, profile_id, assignment_group_id, sha, run_number, run_attempt, created_at, grading_review_id, repository"
      )
      .eq("assignment_id", assignmentId)
      .eq("is_active", true)
      .gt("id", cursor)
      .order("id", { ascending: true })
      .limit(FACT_PAGE_SIZE);
    if (error) throw new CLICommandError(`Failed to load submissions: ${error.message}`, 500);
    if (!rows || rows.length === 0) break;

    for (const row of rows) {
      const submissionRef =
        tokenizer === null ? { id: row.id } : { token: await tokenizer.token("submission", row.id) };

      const subjectRef =
        row.profile_id === null
          ? null
          : tokenizer === null
            ? { id: row.profile_id }
            : { token: await tokenizer.token("subject", row.profile_id) };

      const groupRef =
        row.assignment_group_id === null
          ? null
          : tokenizer === null
            ? { id: row.assignment_group_id }
            : { token: await tokenizer.token("group", row.assignment_group_id) };

      await writer.write({
        kind: "submission",
        ...submissionRef,
        subject: subjectRef,
        group: groupRef,
        sha: row.sha,
        run_number: row.run_number,
        run_attempt: row.run_attempt,
        created_at: row.created_at,
        has_final_review: row.grading_review_id !== null,
        // Repository url is course design metadata, not student PII — same
        // posture as autograder.grader_repo above.
        repository: row.repository
      });
      ids.push(row.id);
      total += 1;
    }

    if (rows.length < FACT_PAGE_SIZE) break;
    cursor = rows[rows.length - 1]!.id;
  }
  return { submissionCount: total, submissionIds: ids };
}

/**
 * Rubric check results from the final grading review of each is_active
 * submission. Spans three tables (general comments, file-line comments,
 * artifact comments) — they share the (submission_review_id, rubric_check_id,
 * points, target_student_profile_id) shape so we normalize them into one
 * "score" record kind with a `surface` discriminator.
 *
 * The grading-review filter happens in two steps:
 *   1. Look up submissions.grading_review_id for the is_active submission ids.
 *   2. Filter each comments table by submission_review_id IN (those ids) AND
 *      rubric_check_id IS NOT NULL (chat-only comments aren't scores).
 */
async function streamScores(
  supabase: ReturnType<typeof getAdminClient>,
  submissionIds: number[],
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> }
): Promise<number> {
  if (submissionIds.length === 0) return 0;

  // Step 1: load grading_review_id for each is_active submission.
  const submissionToReview = new Map<number, number>();
  for (const batch of chunked(submissionIds, 500)) {
    const { data, error } = await supabase.from("submissions").select("id, grading_review_id").in("id", batch);
    if (error) throw new CLICommandError(`Failed to load grading review ids: ${error.message}`, 500);
    for (const r of data ?? []) {
      if (r.grading_review_id !== null) submissionToReview.set(r.id, r.grading_review_id);
    }
  }
  const reviewIds = Array.from(submissionToReview.values());
  if (reviewIds.length === 0) return 0;
  const reviewToSubmission = new Map<number, number>();
  for (const [subId, revId] of submissionToReview.entries()) reviewToSubmission.set(revId, subId);

  let total = 0;
  total += await streamScoresFromTable(
    supabase,
    "submission_comments",
    reviewIds,
    reviewToSubmission,
    mode,
    tokenizer,
    writer,
    "submission"
  );
  total += await streamScoresFromTable(
    supabase,
    "submission_file_comments",
    reviewIds,
    reviewToSubmission,
    mode,
    tokenizer,
    writer,
    "file"
  );
  total += await streamScoresFromTable(
    supabase,
    "submission_artifact_comments",
    reviewIds,
    reviewToSubmission,
    mode,
    tokenizer,
    writer,
    "artifact"
  );
  return total;
}

async function streamScoresFromTable(
  supabase: ReturnType<typeof getAdminClient>,
  table: "submission_comments" | "submission_file_comments" | "submission_artifact_comments",
  reviewIds: number[],
  reviewToSubmission: Map<number, number>,
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> },
  surface: "submission" | "file" | "artifact"
): Promise<number> {
  let total = 0;
  // submission_review_id IN list — Postgres handles up to ~32k values, but to
  // be safe we batch on the review-id side; pagination within a batch is by
  // primary key cursor.
  for (const batch of chunked(reviewIds, 500)) {
    let cursor = 0;
    while (true) {
      const { data: rows, error } = await supabase
        .from(table)
        .select(
          "id, submission_review_id, rubric_check_id, points, comment, target_student_profile_id, released, created_at, deleted_at"
        )
        .in("submission_review_id", batch)
        .not("rubric_check_id", "is", null)
        .is("deleted_at", null)
        .gt("id", cursor)
        .order("id", { ascending: true })
        .limit(FACT_PAGE_SIZE);
      if (error) throw new CLICommandError(`Failed to load ${table}: ${error.message}`, 500);
      if (!rows || rows.length === 0) break;

      for (const row of rows) {
        const submissionId = reviewToSubmission.get(row.submission_review_id!);
        if (submissionId === undefined) continue;

        const submissionRef =
          tokenizer === null ? { id: submissionId } : { token: await tokenizer.token("submission", submissionId) };
        const rubricCheckRef =
          tokenizer === null
            ? { id: row.rubric_check_id }
            : { token: await tokenizer.token("rubric_check", row.rubric_check_id!) };
        const targetSubject =
          row.target_student_profile_id === null
            ? null
            : tokenizer === null
              ? { id: row.target_student_profile_id }
              : { token: await tokenizer.token("subject", row.target_student_profile_id) };

        await writer.write({
          kind: "score",
          surface,
          submission: submissionRef,
          rubric_check: rubricCheckRef,
          target_subject: targetSubject,
          points: row.points,
          comment: row.comment,
          released: row.released,
          created_at: row.created_at
        });
        total += 1;
      }

      if (rows.length < FACT_PAGE_SIZE) break;
      cursor = rows[rows.length - 1]!.id;
    }
  }
  return total;
}

/**
 * grader_result_tests for the is_active submissions of this assignment. Each
 * row carries student_id XOR assignment_group_id (per the schema), preserving
 * the dual-nullability that captures whether a test scored an individual or
 * a group. Output is included only when with_test_output=true and is
 * truncated to test_output_max_bytes — single test outputs can be megabytes.
 *
 * The join goes through grader_results.submission_id rather than
 * grader_result_tests.submission_id directly. Both columns exist on the
 * schema, but the latter is a denormalization that seeded test data leaves
 * NULL; the canonical FK is grader_result_id → grader_results.id.
 */
async function streamGraderTests(
  supabase: ReturnType<typeof getAdminClient>,
  submissionIds: number[],
  mode: IdentityMode,
  tokenizer: Tokenizer | null,
  writer: { write: (record: Record<string, unknown>) => Promise<void> },
  withOutput: boolean,
  outputMaxBytes: number
): Promise<{ testCount: number; graderTestIds: number[] }> {
  if (submissionIds.length === 0) return { testCount: 0, graderTestIds: [] };
  let total = 0;
  const graderTestIds: number[] = [];

  // Step 1: resolve submission_id → grader_result_id list. Most submissions
  // have a single grader_result; multi-attempt runs can have several.
  const graderResultIdToSubmission = new Map<number, number>();
  for (const batch of chunked(submissionIds, 500)) {
    const { data, error } = await supabase
      .from("grader_results")
      .select("id, submission_id")
      .in("submission_id", batch);
    if (error) throw new CLICommandError(`Failed to load grader_results: ${error.message}`, 500);
    for (const r of data ?? []) {
      if (r.submission_id !== null) graderResultIdToSubmission.set(r.id, r.submission_id);
    }
  }
  const graderResultIds = Array.from(graderResultIdToSubmission.keys());
  if (graderResultIds.length === 0) return { testCount: 0, graderTestIds };

  // Step 2: page through grader_result_tests for those grader_result_ids.
  for (const batch of chunked(graderResultIds, 500)) {
    let cursor = 0;
    while (true) {
      const { data: rows, error } = await supabase
        .from("grader_result_tests")
        .select(
          "id, grader_result_id, submission_id, student_id, assignment_group_id, name, part, score, max_score, is_released, name_format, output_format, extra_data" +
            (withOutput ? ", output" : "")
        )
        .in("grader_result_id", batch)
        .gt("id", cursor)
        .order("id", { ascending: true })
        .limit(FACT_PAGE_SIZE);
      if (error) throw new CLICommandError(`Failed to load grader_result_tests: ${error.message}`, 500);
      if (!rows || rows.length === 0) break;

      // The select string is built conditionally to include `output` only when
      // requested; PostgREST's typed select can't see through that string
      // concatenation, so we narrow to the row shape we actually requested.
      const typedRows = rows as unknown as Array<{
        id: number;
        grader_result_id: number;
        submission_id: number | null;
        student_id: string | null;
        assignment_group_id: number | null;
        name: string;
        part: string | null;
        score: number | null;
        max_score: number | null;
        is_released: boolean;
        name_format: string;
        output_format: string | null;
        extra_data: unknown;
        output?: string | null;
      }>;

      for (const row of typedRows) {
        // Resolve submission_id via grader_result_id (canonical FK), falling
        // back to the denormalized submission_id column when present.
        const resolvedSubmissionId = graderResultIdToSubmission.get(row.grader_result_id) ?? row.submission_id ?? null;
        const submissionRef =
          resolvedSubmissionId === null
            ? null
            : tokenizer === null
              ? { id: resolvedSubmissionId }
              : { token: await tokenizer.token("submission", resolvedSubmissionId) };
        const studentRef =
          row.student_id === null
            ? null
            : tokenizer === null
              ? { id: row.student_id }
              : { token: await tokenizer.token("subject", row.student_id) };
        const groupRef =
          row.assignment_group_id === null
            ? null
            : tokenizer === null
              ? { id: row.assignment_group_id }
              : { token: await tokenizer.token("group", row.assignment_group_id) };
        const testRef = tokenizer === null ? { id: row.id } : { token: await tokenizer.token("grader_test", row.id) };

        const record: Record<string, unknown> = {
          kind: "grader_test",
          ...testRef,
          submission: submissionRef,
          subject: studentRef,
          group: groupRef,
          name: row.name,
          part: row.part,
          score: row.score,
          max_score: row.max_score,
          is_released: row.is_released,
          name_format: row.name_format,
          output_format: row.output_format,
          extra_data: row.extra_data ?? null
        };

        if (withOutput) {
          const output = row.output ?? null;
          if (output === null) {
            record.output = null;
          } else if (output.length <= outputMaxBytes) {
            record.output = output;
            record.output_truncated = false;
          } else {
            record.output = output.slice(0, outputMaxBytes);
            record.output_truncated = true;
            record.output_full_bytes = output.length;
          }
        }

        await writer.write(record);
        graderTestIds.push(row.id);
        total += 1;
      }

      if (typedRows.length < FACT_PAGE_SIZE) break;
      cursor = typedRows[typedRows.length - 1]!.id;
    }
  }
  return { testCount: total, graderTestIds };
}

