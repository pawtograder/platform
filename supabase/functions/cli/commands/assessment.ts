interface GradebookExportParams {
  class?: string | number;
  identity_mode?: IdentityMode;
  salt?: string;
  confirm_pii?: boolean;
  /** Optional column selectors. If omitted, all columns' private cells are streamed. */
  gradebook_columns?: Array<string | number>;
}

/**
 * Streams instructor-side gradebook cells for the class. Per the design
 * decision: only is_private = true rows are emitted (those are the
 * instructor-owned values, which are what an instructor analysis cares
 * about), and instructor_only columns are always included regardless of
 * selectors.
 */
async function handleGradebookExport(ctx: MCPAuthContext, rawParams: Record<string, unknown>): Promise<Response> {
  const params = rawParams as unknown as GradebookExportParams;
  const classIdentifier = params.class;
  const mode: IdentityMode = params.identity_mode ?? "opaque";

  if (!classIdentifier) throw new CLICommandError("class is required");
  if (mode === "raw" && params.confirm_pii !== true) {
    throw new CLICommandError(
      "identity_mode=raw exposes real student ids. Re-run with confirm_pii: true to acknowledge.",
      400
    );
  }
  if (mode === "hash" || mode === "opaque") {
    if (!params.salt || typeof params.salt !== "string") {
      throw new CLICommandError(`identity_mode=${mode} requires a salt`);
    }
    if (params.salt.length < 16) {
      throw new CLICommandError(`identity_mode=${mode} requires a salt of at least 16 characters`);
    }
  }

  const supabase = getAdminClient();
  const classData = await resolveClass(supabase, classIdentifier);
  await assertUserCanAccessClass(supabase, ctx.userId, classData.id);

  const tokenizer = mode === "raw" ? null : await createTokenizer(params.salt!);

  return streamNdjson(async (writer) => {
    await writer.write({
      kind: "manifest",
      schema_version: SCHEMA_VERSION,
      identity_mode: mode,
      class: { id: classData.id, slug: classData.slug }
    });

    const { data: cols, error: colErr } = await supabase
      .from("gradebook_columns")
      .select("id, slug")
      .eq("class_id", classData.id);
    if (colErr) throw new CLICommandError(`Failed to load gradebook columns: ${colErr.message}`, 500);

    const candidates = (cols ?? []).map((c) => ({ id: c.id, slug: c.slug }));
    const { resolved, unmatched } = resolveSelectors(params.gradebook_columns, candidates);

    // Same warning contract as the preamble: typoed or wholly-redundant
    // selectors surface as a {kind:"warning"} record rather than silently
    // producing an empty/partial stream. This handler is independently
    // dispatchable, so it can't rely on the preamble having warned first.
    if (unmatched.length > 0) {
      await writer.write({
        kind: "warning",
        scope: "gradebook_columns",
        message: `${unmatched.length} gradebook column selector(s) matched nothing`,
        selectors: unmatched
      });
    }

    const columnIds = new Set(resolved.map((c) => c.id));
    if (columnIds.size === 0) {
      await writer.write({ kind: "end", counts: { gradebook_scores: 0 } });
      return;
    }

    let cursor = 0;
    let total = 0;
    while (true) {
      const { data: rows, error } = await supabase
        .from("gradebook_column_students")
        .select(
          "id, gradebook_column_id, student_id, score, score_override, score_override_note, is_excused, is_missing, is_droppable, released, incomplete_values, is_private"
        )
        .eq("class_id", classData.id)
        .eq("is_private", true)
        .in("gradebook_column_id", Array.from(columnIds))
        .gt("id", cursor)
        .order("id", { ascending: true })
        .limit(FACT_PAGE_SIZE);
      if (error) throw new CLICommandError(`Failed to load gradebook scores: ${error.message}`, 500);
      if (!rows || rows.length === 0) break;

      for (const row of rows) {
        const subjectRef =
          tokenizer === null ? { id: row.student_id } : { token: await tokenizer.token("subject", row.student_id) };
        // Gradebook columns aren't PII (the slug is course design metadata)
        // and `gradebook-columns.json` always emits raw {id, slug, name, ...},
        // so scores reference columns by id too — joining the two files is a
        // direct id match without a tokenization detour.

        await writer.write({
          kind: "gradebook_score",
          subject: subjectRef,
          column: { id: row.gradebook_column_id },
          score: row.score,
          score_override: row.score_override,
          score_override_note: row.score_override_note,
          is_excused: row.is_excused,
          is_missing: row.is_missing,
          is_droppable: row.is_droppable,
          released: row.released,
          incomplete_values: row.incomplete_values
        });
        total += 1;
      }

      if (rows.length < FACT_PAGE_SIZE) break;
      cursor = rows[rows.length - 1]!.id;
    }

    await writer.write({ kind: "end", counts: { gradebook_scores: total } });
  });
}

